export const colorsFormatter = (icon: string) => {
	switch (icon) {
		case "GROCERY":
			return "#7D9768";
		case "MEALS":
			return "#EBCB8B";
		case "SHOPPING":
			return "#c8ccd2";
		case "HOUSING":
			return "#88c0d0";
		case "CAR":
			return "#BF616A";
		case "PHARMACY":
			return "#5E81AC";
	}
};
