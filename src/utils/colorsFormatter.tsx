export const colorsFormatter = (icon: string) => {
	switch (icon) {
		case "grocery":
			return "#7D9768";
		case "snacks":
			return "#EBCB8B";
		case "shopping":
			return "#c8ccd2";
		case "housing":
			return "#88c0d0";
		case "car":
			return "#BF616A";
		case "pharmacy":
			return "#5E81AC";
	}
};
